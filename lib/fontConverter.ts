export const FONT_CATEGORIES = {
  Trending: ["trending"],
  Aesthetic: ["aesthetic"],
  "Gaming & Tech": ["gaming"],
  Dekorativ: ["decorative"],
  Modern: ["modern"],
  Klassisch: ["classic"],
  Vintage: ["vintage"],
  Spezial: ["special"],
  Alle: [],
}

export function getFontsByCategory(category: string): FontMap[] {
  if (category === "Alle") return FONT_STYLES.sort((a, b) => b.popularity - a.popularity)

  const categoryKeys = FONT_CATEGORIES[category as keyof typeof FONT_CATEGORIES] || []
  return FONT_STYLES.filter((font) => categoryKeys.includes(font.category)).sort((a, b) => b.popularity - a.popularity)
}

export function searchFonts(query: string): FontMap[] {
  if (!query.trim()) return FONT_STYLES.sort((a, b) => b.popularity - a.popularity)
  const lowercaseQuery = query.toLowerCase()
  return FONT_STYLES.filter((font) => font.name.toLowerCase().includes(lowercaseQuery)).sort(
    (a, b) => b.popularity - a.popularity,
  )
}

export function convertText(text: string): { name: string; value: string; popularity: number }[] {
  return FONT_STYLES.map((font) => ({
    name: font.name,
    popularity: font.popularity,
    value: Array.from(text)
      .map((char) => {
        if (font.map[char]) {
          return font.map[char]
        }
        return char
      })
      .join(""),
  })).sort((a, b) => b.popularity - a.popularity)
}

export function getTrendingFonts(): FontMap[] {
  return FONT_STYLES.filter((font) => font.popularity >= 90).sort((a, b) => b.popularity - a.popularity)
}

export function getPopularFonts(): FontMap[] {
  return FONT_STYLES.filter((font) => font.popularity >= 85).sort((a, b) => b.popularity - a.popularity)
}
